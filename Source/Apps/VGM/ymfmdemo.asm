;------------------------------------------------------------------------------
; Initialize sound chip and ctc etc
;------------------------------------------------------------------------------
;
init:	
#IF	(debug)
	call	ym_init
#ENDIF
#IF	(ctc_delay)
	call	ctc_config
#ENDIF
